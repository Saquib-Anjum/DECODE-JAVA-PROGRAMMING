package Array;

public class Array_Initialise {
    public static void main(String[] args) {



        int[] arr={1,3,4,6,6,8,7,6,5};
        int size=arr.length;
        for (int i = 0; i < size; i++) {
            System.out.print(arr[i]+" ");


        }
        System.out.println();
        System.out.println(size);
    }
}
