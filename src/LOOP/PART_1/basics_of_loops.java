package LOOP.PART_1;

import java.util.Scanner;

public class basics_of_loops {
    public static void main(String[] args) {
        System.out.println("hello world");
        System.out.println("hello world");
        System.out.println("hello world");
        System.out.println("hello world");
        System.out.println("hello world");
        System.out.println("hello world");
        System.out.println("hello world");
        System.out.println("hello world");
        System.out.println("hello world");

        System.out.println();
        System.out.println();
        Scanner scan = new Scanner(System.in);
        System.out.println("Enter your number  ");
        int n = scan.nextInt();
        for(int i=1;i<=n;i++){
            System.out.println("hello world!");
        }
    }
}
