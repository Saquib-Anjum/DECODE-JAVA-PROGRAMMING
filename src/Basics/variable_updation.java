package Basics;

public class variable_updation {
    public static void main(String[] args){
        int x=60;
        System.out.println(x);
        x = 90+x;
        System.out.println(x);
        x = x*x;
        System.out.println(x);
        x = x-12000;
        System.out.println(x);
        x = x*2;
        System.out.println(x);
        x = 5%4;
        System.out.println(x);


    }


}
