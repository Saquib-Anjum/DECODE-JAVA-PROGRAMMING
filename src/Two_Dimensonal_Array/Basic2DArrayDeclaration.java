package Two_Dimensonal_Array;

public class Basic2DArrayDeclaration {

    public static void main(String[] args) {
        int[][] grid =new int[3][3];
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) System.out.print(grid[i][j] + " ");
            System.out.println();

        }


    }
}
